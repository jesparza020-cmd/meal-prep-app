import type { Ingredient, Macros, Recipe, Slot } from '../types'

type Ing = [name: string, qty: number, unit: string]

function ings(items: Ing[]): Ingredient[] {
  return items.map(([name, qty, unit]) => ({ name, qty, unit }))
}

function mk(
  id: string,
  name: string,
  slot: Slot,
  baseServingLabel: string,
  m: Macros,
  ingredients: Ing[],
  steps: string[],
  minScale = 0.5,
  maxScale = 2.5,
): Recipe {
  return {
    id,
    name,
    slot,
    baseServingLabel,
    perServing: m,
    ingredients: ings(ingredients),
    steps,
    minScale,
    maxScale,
    source: 'seed',
  }
}

